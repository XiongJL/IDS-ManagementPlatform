package com.neu.rule.beans;

public class CountWeekModel {
	private long count;
	public CountWeekModel() {
		super();
	}
	public CountWeekModel(long count) {
		super();
		this.count = count;
	}
	public long getCount() {
		return count;
	}
	public void setCount(long count) {
		this.count = count;
	}
	
}
